#Написати Python-скрипт, який для кола заданого радіуса (вводить користувач з клавіатури) знаходить діаметр, довжину і площу окружності.

radius = float(input("Введiть радiус кола: "))
PI = 3.14
diameter = radius * 2
с = PI * diameter
s = PI * (radius ** 2)
print(f"Резульати: Діаметр: {diameter}, Довжина: {с}, Площа: {s}.")