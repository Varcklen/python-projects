# Попросіть користувача ввести рейтинг обслуговування (від 1 до 5). Виведіть відповідне повідомлення про рівень обслуговування (наприклад, "Відмінно", "Добре", "Задовільно", "Погано", "Жахливо").

rating = int(input("Введiть рейтинг обслуговування (від 1 до 5): "))

rating_name = ["Жахливо", "Погано", "Задовільно", "Добре", "Відмінно"]
if not 1 <= rating <= 5:
    print("Введено не правильне число!")
else:
    print(rating_name[rating - 1])