# Трикутник існує лише тоді, коли сума будь-яких двох сторін більше третьої. Дано: A, B, C - сторони трикутника. Напишіть програму, яка вказує чи існує такий трикутник.

print("Вкажiть длину трьох сторiн трикутника:")

a = float(input("Перша сторона: "))
b = float(input("Друга сторона: "))
c = float(input("Третя сторона: "))

if a + b > c and a + c > b and b + a > c:
    print("Трикутник iснуе!")
else:
    print("Такий трикутник не може iснувати.")