# Попросіть користувача ввести суму покупки. Якщо сума перевищує 1000 гривень, застосуйте знижку у розмірі 10% і виведіть загальну суму до сплати.

sum = int( input("Введiть суму покупки: ") )

DISCOUNT = 0.1

if sum >= 1000:
    sum -= sum * DISCOUNT
    print(f"Використана {DISCOUNT * 100}% знижка!")

print(f"До сплати {sum} гривень.")